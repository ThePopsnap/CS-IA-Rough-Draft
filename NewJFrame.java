    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String firstName = jTextFirstName.getText();
        String lasttName = jTextLastName.getText();
        String id = jTextId.getText();
        double gpa = 0.0;
        studentList.add(new Student(firstName,lasttName,id,gpa));
        dtm.setRowCount(0);
        for (int i = 0; i < studentList.size(); i++) {
            Object[] objs = {studentList.get(i).firstName,studentList.get(i).lastName,studentList.get(i).id,studentList.get(i).gpa};
            dtm.addRow(objs);
        }
        //Resets text boxes
        clearField();        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void clearField(){
        jTextFirstName.requestFocus();
        jTextFirstName.setText("");
        jTextLastName.setText("");
        jTextId.setText("");
        jTextGpa.setText("");
    }    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRowIndex = jTable1.getSelectedRow();
      try{
        dtm.removeRow(selectedRowIndex);
        studentList.remove(selectedRowIndex);
      }catch(Exception ex){
          JOptionPane.showMessageDialog(null, "You have to select a student");
      }
        
    }//GEN-LAST:event_jButton2ActionPerformed
   
