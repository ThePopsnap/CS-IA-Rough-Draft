    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Enter Id");
        for (int i = 0; i < studentList.size(); i++) {
            if (studentList.get(i).id.equalsIgnoreCase(input)) {
                jTextFirstName.setText(studentList.get(i).firstName);
                jTextLastName.setText(studentList.get(i).lastName);
                jTextId.setText(studentList.get(i).id);
                jTextGpa.setText(String.valueOf(studentList.get(i).gpa));
                
                return;
            }else{
                JOptionPane.showMessageDialog(null, "Student Not Found");
            }
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

