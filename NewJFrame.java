    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex1 = jTable1.getSelectedRow();    
        double updatedGpa = studentList.get(selectedRowIndex1).gpa;
        if (updatedGpa < 4.0) {
            updatedGpa += 1.0;
        }else{
            updatedGpa += 0.1;
        }
        studentList.get(selectedRowIndex1).gpa = Math.round(updatedGpa * 10) / 10.0;
        dtm.setRowCount(0);
        for (int i = 0; i < studentList.size(); i++) {
            Object[] objs = {studentList.get(i).firstName,studentList.get(i).lastName,studentList.get(i).id,studentList.get(i).gpa};
            dtm.addRow(objs);
        }
        //Resets text boxes
        clearField();        
       
    }//GEN-LAST:event_jButton4ActionPerformed
